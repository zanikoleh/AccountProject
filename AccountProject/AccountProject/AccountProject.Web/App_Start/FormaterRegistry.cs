using Newtonsoft.Json.Serialization;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Net.Http.Formatting;
using System.Web;
using System.Web.Http;

namespace AccountProject.Web.App_Start
{
    public static class FormaterRegistry
    {
        public static JsonMediaTypeFormatter jsonFormatter;
        public static void Resolve(HttpConfiguration config)
        {
            jsonFormatter = config.Formatters.OfType<JsonMediaTypeFormatter>().First();
            jsonFormatter.SerializerSettings.ContractResolver = new CamelCasePropertyNamesContractResolver();
        }
    }
}